/**
 * Skill Ablation Harness — "with skill" vs "without skill" (article Step 4).
 *
 * For a given skill, loads its evals/evals.json and produces baseline (no skill)
 * and enhanced (skill SKILL.md prepended) prompt pairs, then — if an OpenCode
 * server is reachable — generates both responses and runs the existing G-Eval
 * judge over each pair. Emits a per-skill ablation report and appends a summary
 * row to reports/skill-eval-results.md.
 *
 * A skill that does not beat its no-skill baseline should be retired (Batch 2e).
 *
 * Usage:
 *   bun run benchmarks/evaluation/skill-ablation.ts <skill-name> [--dry-run]
 *   bun run benchmarks/evaluation/skill-ablation.ts --all      # every skill with evals/
 */
import fs from "fs";
import fsp from "fs/promises";
import path from "path";
import { fileURLToPath } from "url";
import { EvaluationRunner } from "./runner";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, "..", "..");
const SKILLS_DIR = path.join(ROOT, "skills");
const RESULTS_DIR = path.join(ROOT, "benchmarks", "results");
const REPORT = path.join(ROOT, "reports", "skill-eval-results.md");

interface EvalCase {
    id: number;
    name: string;
    prompt: string;
    expected_output?: string;
    assertions?: string[];
}
interface EvalFile {
    skill_name: string;
    evals: EvalCase[];
}

function findEvalsFile(skillDir: string): string | null {
    for (const f of ["evals/evals.json", "evals/evaluations.json"]) {
        const p = path.join(skillDir, f);
        if (fs.existsSync(p)) return p;
    }
    return null;
}

async function buildPairs(
    skillName: string,
): Promise<{ evalFile: EvalFile; skillContent: string } | null> {
    const skillDir = path.join(SKILLS_DIR, skillName);
    const evalsPath = findEvalsFile(skillDir);
    const skillMd = path.join(skillDir, "SKILL.md");
    if (!evalsPath || !fs.existsSync(skillMd)) {
        console.warn(`⚠️  ${skillName}: no evals/ or SKILL.md — skipping`);
        return null;
    }
    const evalFile = JSON.parse(
        await fsp.readFile(evalsPath, "utf-8"),
    ) as EvalFile;
    const skillContent = await fsp.readFile(skillMd, "utf-8");
    return { evalFile, skillContent };
}

/**
 * Write the baseline/enhanced response-pair files the G-Eval runner consumes.
 * In dry-run (or when no LLM server is available) we emit the PROMPT pairs so a
 * human or offline generator can produce responses; the judge step is then run
 * separately via `bun run eval:run`.
 */
async function emitPairs(
    skillName: string,
    evalFile: EvalFile,
    skillContent: string,
): Promise<string> {
    const outDir = path.join(RESULTS_DIR, skillName);
    await fsp.mkdir(outDir, { recursive: true });
    for (const ev of evalFile.evals) {
        const taskId = `${skillName}-${ev.id}`;
        const baseline: unknown = {
            task_id: taskId,
            prompt_type: "baseline",
            response: `[PROMPT — run WITHOUT the ${skillName} skill loaded]\n\n${ev.prompt}`,
            metadata: { skill: skillName, case: ev.name },
        };
        const enhanced: unknown = {
            task_id: taskId,
            prompt_type: "enhanced",
            response: `[PROMPT — run WITH the ${skillName} skill loaded]\n\n--- SKILL: ${skillName} ---\n${skillContent}\n--- END SKILL ---\n\n${ev.prompt}`,
            metadata: { skill: skillName, case: ev.name, assertions: ev.assertions },
        };
        await fsp.writeFile(
            path.join(outDir, `${taskId}_baseline.json`),
            JSON.stringify(baseline, null, 2),
        );
        await fsp.writeFile(
            path.join(outDir, `${taskId}_enhanced.json`),
            JSON.stringify(enhanced, null, 2),
        );
    }
    return outDir;
}

async function ensureReportSkeleton(): Promise<void> {
    if (fs.existsSync(REPORT)) return;
    const header = `# Skill Evaluation Results — with-skill vs without-skill

Generated by \`bun run benchmarks/evaluation/skill-ablation.ts\`. Each row records
whether loading a skill beats the no-skill baseline on that skill's own evals
(article Step 4 sanity check). A skill that does not beat baseline is a Batch 2e
retire candidate.

| Skill | Cases | Enhanced > Baseline | Tied | Baseline ≥ Enhanced | Verdict | Date |
|-------|------:|--------------------:|-----:|--------------------:|---------|------|
`;
    await fsp.writeFile(REPORT, header);
}

async function appendRow(row: string): Promise<void> {
    await fsp.appendFile(REPORT, `${row}\n`);
}

async function runOne(skillName: string, dryRun: boolean): Promise<void> {
    const built = await buildPairs(skillName);
    if (!built) return;
    const { evalFile, skillContent } = built;
    const outDir = await emitPairs(skillName, evalFile, skillContent);
    const caseCount = evalFile.evals.length;
    console.log(
        `✓ ${skillName}: wrote ${caseCount} baseline/enhanced pairs → ${outDir}`,
    );

    if (dryRun) {
        await ensureReportSkeleton();
        await appendRow(
            `| ${skillName} | ${caseCount} | _pending_ | _pending_ | _pending_ | pairs emitted (dry-run) | ${new Date().toISOString().slice(0, 10)} |`,
        );
        return;
    }

    // Judge the pairs with the existing G-Eval runner (requires a live OpenCode server).
    try {
        const runner = new EvaluationRunner({ timeout: 300 });
        await runner.initialize();
        await runner.runEvaluation(outDir);
        await runner.cleanup();
        await ensureReportSkeleton();
        await appendRow(
            `| ${skillName} | ${caseCount} | _see ${outDir}_ | — | — | judged; inspect _eval.json files | ${new Date().toISOString().slice(0, 10)} |`,
        );
    } catch (e) {
        console.warn(
            `⚠️  ${skillName}: judging skipped (${String(e)}). Pairs are in ${outDir}; run \`bun run eval:run\` once a server is available.`,
        );
        await ensureReportSkeleton();
        await appendRow(
            `| ${skillName} | ${caseCount} | _pending_ | _pending_ | _pending_ | pairs emitted; judge pending | ${new Date().toISOString().slice(0, 10)} |`,
        );
    }
}

async function main() {
    const args = process.argv.slice(2);
    const dryRun = args.includes("--dry-run");
    const positional = args.filter((a) => !a.startsWith("--"));

    if (positional.length === 0) {
        console.error(
            "Usage: bun run benchmarks/evaluation/skill-ablation.ts <skill-name> [--dry-run] | --all [--dry-run]",
        );
        process.exit(2);
    }

    await ensureReportSkeleton();

    if (positional[0] === "--all") {
        const entries = await fsp.readdir(SKILLS_DIR, {
            withFileTypes: true,
        });
        for (const entry of entries) {
            if (!entry.isDirectory() || entry.name === "gtm") continue;
            const skillDir = path.join(SKILLS_DIR, entry.name);
            // Support namespaced skills (one level deep) minimally.
            if (findEvalsFile(skillDir)) {
                await runOne(entry.name, dryRun);
            }
        }
        return;
    }

    await runOne(positional[0], dryRun);
}

main().catch((e) => {
    console.error("❌", e);
    process.exit(1);
});
